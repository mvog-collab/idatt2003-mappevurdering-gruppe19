package edu.games.engine.model;

/**
 * Represents the available player colors in a Ludo game.
 * Each color typically corresponds to a player's starting position and goal path.
 */
public enum LudoColor {
  /** Green player color */
  GREEN,

  /** Red player color */
  RED,

  /** Blue player color */
  BLUE,

  /** Yellow player color */
  YELLOW
}
